package com.sahil.arrays.concepts;


public class BureauVitasStringConcatination {
	
	String str = "start";
	
public static void main(String[] args) {

	new BureauVitasStringConcatination().BureauVitas();
}
	public void BureauVitas()
	{
		System.out.print(str+": ");
		for (int i = 1; i <= 100; i++) {
			System.out.print(i+" ");
			
		}
	}
}
